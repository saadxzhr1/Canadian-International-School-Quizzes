// ─── HELPERS ──────────────────────────────────────────────────────────────────
function getGrade(gradeId) { return CURRICULUM.grades.find(g => g.id === gradeId); }
function getUnit(gradeId, unitId) { return getGrade(gradeId)?.units.find(u => u.id === unitId); }
function getLesson(gradeId, unitId, lessonId) { return getUnit(gradeId, unitId)?.lessons.find(l => l.id === lessonId); }

function getParams() {
  const p = new URLSearchParams(window.location.search);
  return { grade: p.get('grade'), unit: p.get('unit'), lesson: p.get('lesson') };
}

function getStudents(gradeId, unitId, lessonId) {
  const key = `students_${gradeId}_${unitId}_${lessonId}`;
  const students = JSON.parse(localStorage.getItem(key) || '[]');
  return students.sort((a, b) => a.attempts - b.attempts);
}

function addStudent(gradeId, unitId, lessonId, name, attempts, total) {
  const key = `students_${gradeId}_${unitId}_${lessonId}`;
  const students = JSON.parse(localStorage.getItem(key) || '[]');
  students.push({ name, attempts, total, date: new Date().toLocaleDateString() });
  localStorage.setItem(key, JSON.stringify(students.slice(0, 50)));
}

